nombre_completo = input("Por favor, ingresa tu nombre completo: ")

nombre_minusculas = nombre_completo.lower()
print("Nombre completo en minúsculas:", nombre_minusculas)

nombre_mayusculas = nombre_completo.upper()
print("Nombre completo en mayúsculas:", nombre_mayusculas)

nombre_capitalizado = nombre_completo.title()
print("Nombre completo con primera letra en mayúscula:", nombre_capitalizado)
